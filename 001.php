	<h2>Adolf has a Plan</h2>	
	<p>Back in 1937, some startup politician had an announcement to make. What he said was this: <i>“To provide sufficient food, you must have sparsely settled areas. There is therefore no question of sparing Poland, and the decision remains to attack Poland at the first opportunity.”</i> Later that year, this guy told the county’s generals that <i>“the object of the war is to physically destroy the enemy. That is why I have prepared my 'Death's Head' formations with orders to kill without pity or mercy all men, women, and children of Polish descent or language. Only in this way can we obtain the living space we need.”</i> As you probably understand, that guy was Adolf Hitler. And he was a man with a plan.</p>
	
	<h2>It was an evil plan</h2>
	<p>So the big idea here, was to destroy the Poles. Not to conquer their land, but to literally kill as many people as possible; <a href="https://en.wikipedia.org/wiki/Operation_Tannenberg">Operation Tannenberg</a> is a good example of this. Now, how do you do that? <br> Hitler took a queue from <a href="https://en.wikipedia.org/wiki/Frederick_the_Great">Freddie the Great</a> there: you do that using soldiers and artillery. 
	
	<img src="pic/001/001-5.jpg" class="img-fluid rounded float-lg-left p-3" alt="Yes, I know this picture is from Barbarossa. Stop whining.">
	
	The “mighty Blitzkrieg-panzer” were used to support those two, and were in no way Blitzkrieging. The Oberkommando of the German forces were way too worried that their precious machines would have been attacked from the flanks and/or cut off, so there were never given a strategic mission in this campaign. Traditional and aerial artillery (read: guns and Stukas) were: they were killing Poles.</p>
		
    <p>Now, Hitler, being the political leader of Germany, had <b>demanded</b> that Poland be conquered in six weeks or less. Because of those flanks and, well, because of the last war, the Generals had other ideas: conquering Poland would take several months. But they tried, and came up with Fall Weiss. That attack was planned to… No, let’s take a moment to think about this. The attack was planned. Period. Hitler always maintained that the war against Poland was a defensive war, because of some silly border incident. That incident was completely enacted by German soldiers dressed as Poles. Plus the prepared invasion and, well, genocide plans. Yeah, right. Defensive war, my ass. It’s like Hermann Göring said:</p>

    <p><i>“Naturally the common people don't want war: neither in Russia, nor in England, nor for that matter in Germany. That is understood. But after all, it is the leaders of a country who determine the policy, and it is always a simple matter to drag the people along, whether it is a democracy, or a fascist dictatorship or a communist dictatorship. Voice or no voice, the people can always be brought to do the bidding of the leaders. That is easy. <br> All you have to do is tell them they are being attacked, and denounce the pacifists for lack of patriotism and exposing the country to danger. It works the same in any country.”</i></p>

    <p>Okay, I’m sorry, rant over.  We were talking attack plans. The “German” half of Poland (Molotov-Ribbentrop pact!) was to be conquered from three directions: from Germany itself, from Prussia and from Slovakia. If you want to know what armies were involved you can go look that up on Wikipedia, but I think it’s interesting to note that a nascent AG North and AG South were involved. All those arrows on the map were converging on Warsaw, while the Polish army was to be encircled and destroyed near Radom, on the river Vistula.</p>

	<h2>Witaj w dżungli</h2>
	
	<img src="pic/001/map001-1.jpg" class="img-fluid rounded float-lg-right p-3">
	
	<p>On September 3rd, the third day of the war, General Reinhardt’s 4th Panzer Division broke through the Polish lines. Now, don’t start thinking Tigers, halftracks and Hummels. No, this is 1939. German armour consisted mostly of Panzer I and IIs, accompanied by foot soldiers and horse drawn artillery. Still, the road to Warsaw was open. The Polish civil servants started by -very predictably - running away. Improvised defences were organised, commanded by general <a href="https://en.wikipedia.org/wiki/Walerian_Czuma">Walerian Czuma</a> and in parallel, by the Polish communist party: the PPS. Only four divisions of infantry were available, so the civilian population was asked to make barricades and help defend the city. And they did! The civilians didn’t have a lot of weapons but they were reinforced by regular infantry, who had anti-tank guns. Famously, at one point they covered one of the main streets with gasoline. When their enemy came near, the road was set on fire which caused heavy casualties and destroyed quite a lot of tanks. This kind of thing - irregular warfare- really worried the Germans who were more set up for traditional army-to-army fighting in the old 1800’s style. They had a lot to learn and, it has to be said, they realised this. But more on that later.</p>

    <p>Meanwhile, the communists were running around, trying to order the proletariat to unite. But the masses didn’t listen: they were too busy fighting Germans. This really annoyed <a href="https://pl.wikipedia.org/wiki/Marian_Kenig">mr. Kenig</a>, the PPS’s leader of the <a href="https://pl.wikipedia.org/wiki/Robotnicza_Brygada_Obrony_Warszawy">Worker's Defense Brigade</a> who felt that the masses should do what they were told. Still, they all had the same goal and in the end the PPS too did a good job.</p>
	
	<h2>Mr. Ziegler crashes the party</h2>
	<p>On September 12th, 1700 hours, the German assault started. Their 4th Panzer Division was let loose on Warsaw's western borough of Ochota. They already knew to expect strong resistance: air reconnaissance had discovered a lot of barricades. After a good night's sleep in their well-protected camp outside the city, a certain Hauptfeldwebel Ziegler picks up the narrative.</p>

    <i><p>"My mission, with two other tanks, is to protect the company commander and, if necessary, establish liaison with the sections, or reconnoiter. Everything inside me vibrates; my apprehension has disappeared, and look forward to the attack."</p></i>
	
    <p>But the first assault was repulsed and the German forces suffered heavy casualties. A lot of their precious tanks were lost, mainly because they didn’t have enough infantry support. The Poles had hidden their 75mm anti-tank guns well and fired into the Germans at point-blank range. So the next day, when their infantry caught up, another German assault towards Ochota and Wola was launched. And once more, the Polish guns and barricades successfully repelled all attacks against the city.</p>
    
	<img src="pic/001/001-3.jpg" class="img-fluid carousel-inner"> 
	
    <br><br><i><p>“I glance through the observation slit and see a civilian running toward us. A sudden movement of his arm - a grenade flies over and bursts on us without doing any damage. He doesn’t have time to throw a second one because my gun cuts him to bits. Two hundred yards further along, on a railway embankment, about fifty Poles scatter, running. My machine gun fires again. A hail of bullets mows down the enemy. While spraying suspicious points with lead, I suddenly see, halfway to my left in a garden, a burst of flame and I hear the explosion of a shell. The munition depot of a 75mm gun, which was in position ready to fire on us, has been accidentally hit. The entire gun crew has disappeared.”</p></i>

    <p>Those Poles near the railway were trying to get to the supply trains parked at the railheads. There were dozens of trains loaded with various kinds of military supplies. Practically everything they needed was right there: weapons, ammunition, uniforms and food. But, as you might expect, unloading is difficult when you’re being shot at. They did manage to get some stuff out at night, but the Poles’ logistical situation was a nightmare. Most stocks were burning because of bombardments by gun and Stuka and what was there couldn’t be reached.</p>

        <img src="pic/001/001-4.jpg" class="img-fluid rounded float-lg-right p-3">
    
    <i><p>“And now, directly in front of us, an anti-tank obstacle looms up. There is no way to avoid it; we must go across. Carefully, the tank on my right approaches it and suddenly, all hell breaks loose. In front of us several shells burst in quick succession. An anti-tank is firing from an unknown direction. I look for it and fire as fast as I can. While changing the machine gun’s magazine I glance around me. The two light tanks are in flames. Is there another gun behind us? Maybe it’s an enemy tank or another 75mm-gun! I haven’t time to think very long. I order the tank beside me to return along the road by which we came and keep firing at the barricade in front of me. Then, I try to follow my colleague.</p>

    <p>But then, his engine is hit - but the shell did not burst. I grit my teeth and press my head against the gunsight, searching for the enemy. The Poles keep firing on us but the smokes from our wrecked tanks obscure their view. Most of their fire falls short but then a shell whistles under the tank. It tears out part of the motor chassis, and its explosion lifts us off our springs. We reach the main road where several tanks of our group are already assembled. From the town comes a steady artillery fire that has put several tanks of our regiment out of action by direct hits.</p>

    <p>Finally we reach our original line of departure and some comrades are already there. Their tanks were destroyed by guns or mines, and they returned on foot. They inform me that several of my men are dead, burning in destroyed tanks or shot by gunfire. The attack lasted five hours but failed on account of the city’s powerful defenses.”</p></i>
    
	
	
	<h2>Lessons learned</h2>
    <p>The supposedly mighty German army kind of failed in Poland. The thing is, they were aware of that. Self-criticism is usually a good thing, especially when it drives you to do things better. The campaign clearly showed the Panzer Divisions were a good approach to warfare but a lot was left to be desired. Their troops were lacking in discipline, camouflage was insufficient and their fire control was none too good either. The reasons for this are obvious: the Germans had a very small but elite army in the interwar years, thanks to the Versailles restrictions. That meant, only the very best were admitted to the army and those were trained to perfection. But when Adolf started expanding the military, he did so far too quickly and a lot of troops were still raw, new to their jobs. As a result, general von Brauschitz, head of the OKH, ordered a dramatic restructuring at every level of their organization. Loads of NCOs and mid-level commanders were re-trained by the old hands during the winter of 1939. Also, the Panzer Divisions were systematically reinforced by motorized infantry and self-propelled artillery.</p>
    <p>The resulting army was the famous one who crushed the French and drove the British to the Dunkerque beaches. But let’s not forget: the bulk of their army was still using horses for just about everything.</p>
	
	
	
	



