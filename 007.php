	<h2>Toy Soldiers</h2>	
	<p>When I was a boy, I had this silly idea that an army acted like my toy soldiers: unthinking pawns with me bossing them around. Now that I'm older I know this isn't true at all. Soldiers have feelings too. And sometimes -actually, quite often- the outcome of a battle or entire campaign is dependant on the actions of only a few men. For example, I believe the pace in the Battle for France was really set by four people: Erich von Manstein, Heinz Guderian, Erwin Rommel and Hugo Sperrle. But everyone knows them (<i>...in my world, everybody does. Basta!</i>) and I like finding others. So, in Guderian's "Panzer Leader", I read about a certain Luitenant Felix who thought he'd tell his General what to do. Let's go to Poland, then, and meet the man.</p>
    
	
	
	<h2>Guderian's home town</h2>
    <p><i>"The first serious fighting took place north of Zempelburg (Bydgoszcz) in and around Gross-Klonia (Wielka Klonia), where the mist suddenly lifted and the leading tanks found themselves face to face with Polish defensive positions."</i>, Guderian writes.         
    <img src="pic/007/map.png" class="img-fluid rounded float-lg-left p-3"> 
    <i>"The Polish anti-tank gunners scored many direct hits. One officer, one officer cadet and eight other ranks were killed."</i></p>


    <p>The village of Gross-Klonia had once been the property of the Guderian family. He was coming home with 3. PD in tow. Pressing forward, as was his manner, he found PR6 quite inert nearby Pruszcz because, well, they were eating sausages. The Regimental commander, whose superior had been flown out to see von Bock (CinC of their Army Group), <i>"did not believe that a passage of the river could be forced on that day, and he was eager to carry out the welcome orders for a rest. The corps order - that the Brahe (Brda) should be crossed during the first day of the attack - had been forgotten. I walked angrily away and tried to decide what measures I should take to improve this unhappy state of affairs."</i></p>
	
	<h2>Tea and sausage</h2>
    <img src="pic/007/guderian.png" class="img-fluid rounded float-lg-right p-3">
    <p>What? Let's have tea? Sausage, anyone? Is this the mighty blitzkrieg machine? The steamrolling conqueror crushing all before it? Did they bring the mustard? Fortunately, while old Heinz was still being angry, a Leutnant Felix rolled up in his Panzer II, jumped out and dusted himself off. <i>"'Herr General,' he said, 'I've just come from the Brahe. The enemy forces on the far bank are weak. The Poles set fire to the bridge at Hammermuhle (Sokole Kuznica), but I put the fire out from my tank. The bridge is crossable. The advance has only stopped because there's no one to lead it. You must go there yourself, sir.' I looked at the young man in amazement. He made a very good impression and his eyes inspired confidence."</i> Guderian followed the Leutnant in his command vehicle <i>"through a confusion of German and Polish vehicles along the narrow sandy track that led through the woods to Hammermuhle. A group of staff officers were standing behind a stout oak tree about 100 yards from the water's edge. They greeted me with the cry: 'Herr General, they're shooting here!'"</i>.</p>
    

    
    <p>Indeed, there was shooting. PR6's Panzer were happily "blazing away" at the enemy, accompanied by 3. Rifle regiment. The enemy, cowering in a trench on the opposite side of the river, wasn't really shooting back. <i>"First of all I put a stop to the idiotic firing. Then I ordered that the extent of the enemy's defensive positions be established. Motorcycle Battalion 3, which had not yet been in action, was sent across the river in rubber boats at a point that was not under enemy fire. When they had crossed successfully, I ordered the tanks over the bridge. They took the Polish bicycle company, which was defending this sector of the stream, prisoner. Casualties were negligible."</i></p>

    <h2>Self-reflection is a good thing</h2>
    <p>So there. These silly people were taking a nap because their enemy -a bicycle company- had shot some rifles at their precious panzer tanks. Two men made the difference. By the way, this lack of performance was noted - I already mentioned this in Zieglers story. The Oberkommando saw they were in no way ready to start a major war against the French - who were a lot stronger than the Poles. Basically, if the Germans hadn't nearly lost in Poland, they would never have won the Battle for France. It would probably have turned into another WW1-slugfest, something no-one wanted.</p> 
	
